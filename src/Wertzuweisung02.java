public class Wertzuweisung02 {
    public static void main(String[] args) {
        int i;
        int j;
        i = 6;
        j = 10;
        System.out.println(i);
        System.out.println(j);
        System.out.println();

        j = i + 10;
        System.out.println(i);
        System.out.println(j);
    }
}
